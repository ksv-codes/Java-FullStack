package A4OperatorsIfelseNoSys.Lecture;

public class ArithmaticOp {
    public static void main(String[] args) {
        int a =8;
        int b =5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);//reminder (only for Integers)
        System.out.println();
        double x=8;
        double y=5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
