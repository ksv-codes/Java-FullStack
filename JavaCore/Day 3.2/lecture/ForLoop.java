package seven.lecture;

public class ForLoop {
    public static void main(String[] args) {
        int i =0;
        while(i<=99){
            System.out.println(i+1);
            i++;
        }
        System.out.println();
        for (int j = 1; j <= 100; j++) {
            System.out.println(j);
        }
    }
}
